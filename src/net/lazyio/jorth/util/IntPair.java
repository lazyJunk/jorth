package net.lazyio.jorth.util;

public class IntPair {

    public int a;
    public int b;

    public IntPair(int a, int b) {
        this.a = a;
        this.b = b;
    }

    @Override
    public String toString() {
        return "IntPair{" +
                "a=" + a +
                ", b=" + b +
                '}';
    }
}
